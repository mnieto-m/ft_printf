#include "ft_printf.h"

int	ft_putstr_pf(char *s)
{
	int i;
	int count;

	count = 0;
	i = 0;
	while(s[i] != '\0')
	{
		count += write(1, &s[i], 1);
		i++;
	}
	return(count);
}
